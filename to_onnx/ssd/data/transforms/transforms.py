class ToAbsoluteCoords(object):
    def __call__(self, image, boxes=None, labels=None):
        if Empty(boxes):
            return image, boxes, labels
        height, width, channels = image.shape
        boxes[:, 0] *= width
        boxes[:, 2] *= width
        boxes[:, 1] *= height
        boxes[:, 3] *= height

        return image, boxes, labels

